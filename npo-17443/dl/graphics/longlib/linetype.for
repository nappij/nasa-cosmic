C
C *** LAST REVISED ON 17-AUG-1987 07:21:09.67
C *** SOURCE FILE: [DL.GRAPHICS.LONGLIB]LINETYPE.FOR
C
C	PROGRAM TO DEMONSTRATE THE AVAILABLE LINE TYPES AND COLORS
C
	CALL FRAME(3,0,0.2,0.3,1.)
C
C	PLOT A PAGE TITLE
C
	CALL SYMBOL(.15,.1,.15,'(Type,Width,Color) Available Line Types'
     $		,90.,39,-1)
C
C	PLOT A SAMPLE OF EACH LINE TYPE, WIDTH, AND COLOR
C
	ICNT=0
	DO 100 IWIDTH=1,5
		DO 100 ITYPE=0,9
			DO 100 ICOLOR=1,6
C
10	X=(ICNT/35)*1.7+.21
	Y=MOD(ICNT,35)*.2
	IF (X.GT.10.0) THEN		! NEW PAGE
		CALL NEWPAGE
		CALL RTERM(2)
		CALL CTERM(2)
		CALL CTERM(-1)
		ICNT=0
		GOTO 10
	ENDIF
	ICNT=ICNT+1
C
C	SET TO SOLID LINE
C
	CALL NEWPEN(0)
	CALL PLOT(1.,0.,0)
	CALL NUMBER(X,Y,.15,FLOAT(ITYPE),0.,-1,-1)
	CALL SYMBOL(999.,999.,.15,',',0.,1,-1)
	CALL NUMBER(999.,999.,.15,FLOAT(IWIDTH),0.,-1,-1)
	CALL SYMBOL(999.,999.,.15,',',0.,1,-1)
	CALL NUMBER(999.,999.,.15,FLOAT(ICOLOR),0.,-1,-1)
C
	CALL PLOT(FLOAT(ICOLOR),0.,0)
	CALL NEWPEN(ITYPE+10*IWIDTH)
	X=X+0.8
	CALL PLOT(X,Y,3)
	CALL PLOT(X+.75,Y,2)
	CALL PLOT(X,Y,3)
C
100	CONTINUE
C
	CALL CTERM(2)
	CALL RTERM(2)
	CALL PLOTND
	STOP
	END
