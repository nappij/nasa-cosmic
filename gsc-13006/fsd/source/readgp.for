      SUBROUTINE READGP
C
      IMPLICIT REAL*8(A-H,O-Z)
C
      COMMON/INOMOT/ NOINTG,NOIOUT(20)
C
      CALL GMBLRD
C
      CALL GMBDRD
C
      CALL SAGMRD
C
      CALL THRMRD
C
      CALL WHREAD
C
      CALL VDMPRD
C
      CALL AWREAD
C
      CALL TRQORD
C
      CALL SETUP(8HNOINTG  ,4,NOINTG)
      CALL SETUP(8HNOIOUT  ,4,NOIOUT,20)
C
      RETURN
C
      END
